public class VideoObject {
	
	public String name;
	public String desc;
	public String fileid;
	public int ID;

	public VideoObject(String name, String desc, String fileid, int ID) {
		this.name = name;
		this.desc = desc;
		this.fileid = fileid;
		this.ID = ID;
	}
}
