package com.zeshanaslam.ayc.listviews.video;

public class VideoObject {

    public int ID;
    public String year;
    public String section;
    public String name;
    public String desc;

    public VideoObject(int ID, String year, String section, String name, String desc) {
        this.ID = ID;
        this.year = year;
        this.section = section;
        this.name = name;
        this.desc = desc;
    }
}
